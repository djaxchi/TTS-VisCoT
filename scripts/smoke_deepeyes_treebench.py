"""Smoke-test: run DeepEyesV2 on a single TreeBench question and print the full trace.

Any matplotlib figures generated by code executed in the CoT are saved to
  results/smoke_deepeyes/<turn_N_fig_M>.png
so they can be inspected after the run.
"""

import sys
import textwrap
import time
from pathlib import Path

import numpy as np

# Make sure the repo root is on sys.path.
sys.path.insert(0, str(Path(__file__).resolve().parents[1]))

from src.data.datasets.treebench import TreeBenchDataset
from src.models.deepeyes_v2 import (
    DeepEyesV2Model,
    _execute_code,
    _extract_code_block,
    _fix_python_indentation,
)

OUT_DIR = Path(__file__).resolve().parents[1] / "results" / "smoke_deepeyes"
OUT_DIR.mkdir(parents=True, exist_ok=True)

# ---------------------------------------------------------------------------
# 1. Load one TreeBench example
# ---------------------------------------------------------------------------
print("=" * 70)
print("Loading TreeBench (train split, 1 sample)…")
ds = TreeBenchDataset(split="train", max_samples=1)
ds.load()
ex = ds[0]

question = ex["question"]
options  = ex["options"]
image    = ex["image"]
gt       = ex["correct_answer"]
meta     = ex["metadata"]

# Build the full MCQ prompt that the model will receive.
option_lines = "\n".join(f"  {k}: {v}" for k, v in sorted(options.items()))
full_query = f"{question}\n\nOptions:\n{option_lines}"

print(f"\nQuestion  : {question}")
print(f"Options   :\n{textwrap.indent(option_lines, '  ')}")
print(f"GT answer : {gt}")
print(f"Metadata  : {meta}")
print(f"Image size: {image.size if image else 'N/A'}  mode: {image.mode if image else 'N/A'}")

# ---------------------------------------------------------------------------
# 2. Run DeepEyesV2
# ---------------------------------------------------------------------------
print("\n" + "=" * 70)
print("Loading DeepEyesV2 model…")
model = DeepEyesV2Model(load_in_8bit=True)

print("Running inference (n=1, greedy)…")
_t0 = time.perf_counter()
results = model.generate(image, full_query, n=1, temperature=0.0)
_inference_s = time.perf_counter() - _t0
chain = results[0]
print(f"Inference time: {_inference_s:.1f}s  ({len(chain['cot_steps'])} turns)")

# ---------------------------------------------------------------------------
# 3. Re-execute code blocks to capture figures
# ---------------------------------------------------------------------------
# The model already ran the code internally; we replay each code block here
# with the same initial namespace so we can intercept the figures.
import PIL
import math
import collections

pil_image = image.convert("RGB")
replay_ns = {
    "np": np,
    "PIL": PIL,
    "image_1": pil_image,
    "image": pil_image,
    "math": math,
    "collections": collections,
}

saved_figures: dict[int, list[Path]] = {}  # turn → [saved paths]

for turn_idx, step in enumerate(chain["cot_steps"]):
    code = _extract_code_block(step)
    if code is None:
        continue
    code = _fix_python_indentation(code)
    _, _, figures = _execute_code(code, replay_ns)
    paths: list[Path] = []
    for fig_idx, fig_bytes in enumerate(figures):
        fname = OUT_DIR / f"turn_{turn_idx + 1}_fig_{fig_idx + 1}.png"
        fname.write_bytes(fig_bytes)
        paths.append(fname)
        print(f"  Saved figure -> {fname}")
    if paths:
        saved_figures[turn_idx + 1] = paths

# ---------------------------------------------------------------------------
# 4. Pretty-print the full trace
# ---------------------------------------------------------------------------
print("\n" + "=" * 70)
print("FULL CHAIN TRACE")
print("=" * 70)

cot_steps    = chain["cot_steps"]
tool_results = chain["tool_results"]
answer       = chain["answer"]

print(f"\nFinal answer : {answer}")
print(f"GT answer    : {gt}")
print(f"Correct?     : {answer.strip().upper() == gt.strip().upper()}")

print(f"\n--- CoT steps ({len(cot_steps)}) ---")
for i, step in enumerate(cot_steps, 1):
    print(f"\n[Turn {i} — model output]")
    print(textwrap.indent(step, "  "))
    if i - 1 < len(tool_results):
        print(f"\n[Turn {i} — tool result]")
        print(textwrap.indent(tool_results[i - 1], "  "))
    if i in saved_figures:
        for p in saved_figures[i]:
            print(f"\n[Turn {i} -- figure saved -> {p}]")

print("\n" + "=" * 70)
print("Done.")
