"""DeepEyesV2 model wrapper — Qwen2.5-VL with agentic tool-use CoT.

DeepEyesV2 (honglyhly/DeepEyesV2_7B_1031) is a Qwen2.5-VL-7B model
fine-tuned via SFT + GRPO reinforcement learning to perform multi-turn
reasoning using a Python code-execution tool.

The model's reasoning loop:
1. Receives system prompt + image + user query.
2. Optionally executes Python code (``<code>...</code>``) to inspect the image.
3. Repeats until it emits ``<answer>...</answer>``, which terminates the loop.

This module exposes:
- :class:`DeepEyesV2Model` — ``BaseVisualCoTModel`` implementation.
- :func:`_parse_answer` — extract ``<answer>`` tag content.
- :func:`_extract_code_block` — extract ``<code>`` tag content.
- :func:`_execute_code` — run Python code with an isolated namespace.
"""

from __future__ import annotations

import base64
import contextlib
import io
import os
import re
from typing import Any, Dict, List, Optional

# Force a non-interactive matplotlib backend before any user code can import
# matplotlib.  Without this, plt.show() inside the code sandbox opens a GUI
# window and blocks execution indefinitely.
os.environ.setdefault("MPLBACKEND", "Agg")

import torch
from PIL import Image

from src.models.base import BaseVisualCoTModel
from src.utils.logging import get_logger

logger = get_logger(__name__)

# ---------------------------------------------------------------------------
# Constants
# ---------------------------------------------------------------------------

DEFAULT_MODEL_ID = "honglyhly/DeepEyesV2_7B_1031"
DEFAULT_MAX_TURNS = 10

# ---------------------------------------------------------------------------
# Exact prompts from the paper (Appendix A.8 of arXiv:2511.05271).
# Search-tool instructions are omitted; only the Python-code tool is kept.
# ---------------------------------------------------------------------------

SYSTEM_PROMPT = """\
You are an agent - please keep going until the user's query is completely resolved, before \
ending your turn and yielding back to the user. Only terminate your turn when you are sure \
that the problem is solved.

Solve the following problem step by step. In your reasoning process, if the answer cannot be \
determined, you can write Python code in a Jupyter Notebook to process the image and extract \
more information from it. The stdout and stderr content, along with the images generated by \
"plt.show()" will be returned to better assist with the user query.

You MUST use the python tool to analyze or transform images whenever it could improve your \
understanding. This includes but is not limited to zooming in, rotating, adjusting contrast, \
computing statistics, or isolating features.

You MUST plan extensively before each function call, and reflect extensively on the outcomes \
of the previous function calls. DO NOT do this entire process by making function calls only, \
as this can impair your ability to solve the problem and think insightfully.

For all the provided images, in order, the i-th image has already been read into the global \
variable "image_i" using the "PIL.Image.open()" function. For example, the first image can \
be accessed as "image_1". When writing Python code, you can directly use these variables \
without needing to read them again.

## Tools

## python
Your python code should be enclosed within <code> </code> tag.
Example for calling Python code in Jupyter Notebook:
<code>
'''python
# python code here
'''
</code>

Note:
1. python can be called to analyze the image. python will respond with the output of the \
execution or time out after 300.0 seconds.
2. Like jupyter notebook, you can use Python code to process the input image and use \
"plt.show()" to visualize processed images in your code.
3. All python code are running in the same jupyter notebook kernel, which means the \
functions and variables are automatically stored after code execution.
4. You program should always returns in finite time. Do not write infinite loop in your code.
5. Writing file to disk is not allowed.
"""

# USER_PROMPT_TEMPLATE: wraps each user query.
# This is the exact per-question template from the paper — it is what instructs
# the model to emit <answer> tags and use the <think>/<code>/<answer> format.
USER_PROMPT_TEMPLATE = """\
{question}

You must put your answer inside <answer> </answer> tags, i.e., <answer> answer here </answer>.
Please reason step by step. Use Python code to process the image if necessary.

Format strictly as <think> </think> <code> </code> (if code is needed)
or <think> </think> <answer> </answer> (if answer is ready).
"""

# RETURN_CODE_USER_PROMPT: exact template injected after each code execution.
# Triple-quote delimiters are on their own lines, exactly as in the paper.
RETURN_CODE_USER_PROMPT = """\
Code execution result:
stdout:
'''
{stdout}
'''
stderr:
'''
{stderr}
'''
Image:
"""

# ---------------------------------------------------------------------------
# Module-level helpers (public for testability)
# ---------------------------------------------------------------------------

_ANSWER_RE = re.compile(r"<answer>(.*?)</answer>", re.DOTALL)
_CODE_RE = re.compile(r"<code>(.*?)</code>", re.DOTALL)
# Matches the '''python ... ''' wrapper the model places inside <code> tags.
_PYTHON_TRIPLE_QUOTE_RE = re.compile(r"^\s*'''python\s*\n(.*?)\n\s*'''\s*$", re.DOTALL)
# Backtick fence *inside* a <code> tag: ```python ... ```
_BACKTICK_FENCE_RE = re.compile(r"^\s*```python\s*\n(.*?)\n\s*```\s*$", re.DOTALL)
# Top-level bare backtick fence (model output without <code> wrapper).
_BARE_FENCE_RE = re.compile(r"```python\s*\n(.*?)\n```", re.DOTALL)


def _parse_answer(text: str) -> Optional[str]:
    """Extract the first ``<answer>...</answer>`` span, stripped, or ``None``.

    Args:
        text: Raw model output string.

    Returns:
        Stripped answer text, or ``None`` if no ``<answer>`` tag is found.
    """
    m = _ANSWER_RE.search(text)
    return m.group(1).strip() if m else None


def _extract_code_block(text: str) -> Optional[str]:
    """Extract the first ``<code>...</code>`` span, stripping any Python markdown wrapper.

    The model wraps code in a ``'''python ... '''`` (or `` ```python ... ``` ``) fence
    *inside* the ``<code>`` tag.  Returning that wrapper verbatim to ``exec()`` would
    make the code a silent no-op (Python evaluates it as a string literal).  We strip
    the wrapper so the caller receives executable Python.

    Args:
        text: Raw model output string.

    Returns:
        Executable Python source (indentation preserved), or ``None`` if no tag found.
    """
    # Priority 1: explicit <code>...</code> tags (training format).
    m = _CODE_RE.search(text)
    if m:
        raw = m.group(1)
        for pattern in (_PYTHON_TRIPLE_QUOTE_RE, _BACKTICK_FENCE_RE):
            wrapper_m = pattern.match(raw)
            if wrapper_m:
                return wrapper_m.group(1)
        return raw
    # Priority 2: bare ```python ... ``` fence (model's fallback output format).
    bare_m = _BARE_FENCE_RE.search(text)
    if bare_m:
        return bare_m.group(1)
    return None


def _execute_code(code: str, namespace: Dict[str, Any]) -> str:
    """Execute *code* in *namespace* and return captured stdout or error info.

    Args:
        code: Python source code to execute.
        namespace: Execution namespace; modified in-place by the code.

    Returns:
        Captured stdout (trailing newlines stripped), or an error summary
        of the form ``"ExceptionType: message"`` on failure.
    """
    buf = io.StringIO()
    try:
        with contextlib.redirect_stdout(buf):
            exec(code, namespace)  # noqa: S102
    except Exception as exc:  # noqa: BLE001
        return f"{type(exc).__name__}: {exc}"
    return buf.getvalue().rstrip("\n")


